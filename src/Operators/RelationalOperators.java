package Operators;

public class RelationalOperators {

	public static void main(String[] args) {
		
		int a =10;
		int b =5;
		
		boolean result1 = a == b;
		System.out.println(result1);
		
		
		boolean result2 = a > b;
		System.out.println(result2);
		
		
		boolean result3 = a < b;
		System.out.println(result2);
		
		boolean result4 = a >= b;
		System.out.println(result4);
		
		boolean result5 = a <= b;
		System.out.println(result5);
		
		
		boolean result6 = a != b;
		System.out.println(result6);

	}

}
