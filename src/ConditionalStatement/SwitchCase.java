package ConditionalStatement;

public class SwitchCase {

	public static void main(String[] args) {
		String fruit = "Mango";
		switch(fruit) {
		case "Mango":
			System.out.println("Mango");
			break;
		case "Apple":
			System.out.println("Apple");
			break;
		case "Papaya":
			System.out.println("Papaya");
			break;
		default:
			System.out.println("Fruits");
		}
	}

}
